const state = {
  token: "",
  userName: "",
};
const mutations = {
  // tslint:disable-next-line:no-shadowed-variable
  setUser(state: any, { userName, token }: { userName: string, token: string }): void {
    state.userName = userName;
    state.token = token;
  },
};
const getters = {
// tslint:disable-next-line: no-shadowed-variable
  token(state: any) {
    return state.token;
  },
};
export default {
  state,
  mutations,
  getters,
};
